KINDS = {
    "vec":     VEC_TEMPLATE,
    "ring":    RING_TEMPLATE,
    "map_u64": MAP_U64_TEMPLATE,
    "map_str": MAP_STR_TEMPLATE,
}


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--kind", required=True, choices=sorted(KINDS.keys()))
    ap.add_argument("--type-name", required=True,
                    help="Identifier prefix for generated types/functions, e.g. 'event'")
    ap.add_argument("--value-type", required=True,
                    help='C value type, e.g. "int32_t" or "struct event *"')
    ap.add_argument("--output", required=True, type=Path,
                    help="Output header path")
    args = ap.parse_args()

    if not re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", args.type_name):
        print(f"error: --type-name must be a C identifier: {args.type_name!r}",
              file=sys.stderr)
        return 2

    args.output.parent.mkdir(parents=True, exist_ok=True)
    template = KINDS[args.kind]
    rendered = template.format(
        guard=header_guard(args.output),
        name=args.type_name,
        NAME=args.type_name.upper(),
        value=args.value_type,
    )
    args.output.write_text(rendered, encoding="utf-8")
    print(f"wrote {args.output}  ({args.kind} of {args.value_type})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
