MAP_U64_TEMPLATE = r"""/* AUTO-GENERATED by tools/generate_container_header.py. Do not edit. */
#ifndef {guard}
#define {guard}

#include <stddef.h>
#include <stdint.h>
#include <stdlib.h>
#include <string.h>

#include "handy/handy_safety.h"

#ifdef __cplusplus
extern "C" {{
#endif

/* Open-addressing hash map with linear probing and tombstones.
 * Capacity is always a power of two; grows when load (count+tombstones) > 7/8. */

#define {NAME}_MAP_STATE_EMPTY     0u
#define {NAME}_MAP_STATE_USED      1u
#define {NAME}_MAP_STATE_TOMBSTONE 2u

typedef struct {{
    uint64_t key;
    {value} value;
    unsigned char state;
}} {name}_map_entry;

typedef struct {{
    {name}_map_entry *entries;
    size_t capacity;       /* power of two */
    size_t count;
    size_t tombstones;
}} {name}_map_u64;

static inline uint64_t {name}_map_hash_(uint64_t x) {{
    x ^= x >> 30; x *= 0xbf58476d1ce4e5b9ull;
    x ^= x >> 27; x *= 0x94d049bb133111ebull;
    x ^= x >> 31; return x;
}}

static inline HS_NODISCARD hs_status {name}_map_u64_init({name}_map_u64 *m, size_t initial_capacity) {{
    size_t cap = 16;
    while (cap < initial_capacity) {{ cap *= 2; }}
    m->entries = ({name}_map_entry *)calloc(cap, sizeof(*m->entries));
    if (m->entries == NULL) {{ return HS_ERR_CAP; }}
    m->capacity = cap; m->count = 0; m->tombstones = 0;
    return HS_OK;
}}

static inline void {name}_map_u64_free({name}_map_u64 *m) {{
    if (m->entries != NULL) {{ free(m->entries); }}
    m->entries = NULL; m->capacity = 0; m->count = 0; m->tombstones = 0;
}}

static inline size_t {name}_map_u64_len(const {name}_map_u64 *m) {{ return m->count; }}

/* Find slot for key. Returns index. If found, slot is USED with matching key.
 * If not found, slot is the first EMPTY slot in the probe chain (insert here),
 * unless include_tombstones=1 in which case the first TOMBSTONE is preferred
 * (for inserts). */
static inline size_t {name}_map_find_slot_(const {name}_map_u64 *m, uint64_t key,
                                            unsigned char for_insert) {{
    size_t mask = m->capacity - 1;
    size_t idx = (size_t){name}_map_hash_(key) & mask;
    size_t first_tomb = (size_t)-1;
    for (;;) {{
        unsigned char st = m->entries[idx].state;
        if (st == {NAME}_MAP_STATE_EMPTY) {{
            if (for_insert && first_tomb != (size_t)-1) {{ return first_tomb; }}
            return idx;
        }}
        if (st == {NAME}_MAP_STATE_USED && m->entries[idx].key == key) {{
            return idx;
        }}
        if (st == {NAME}_MAP_STATE_TOMBSTONE && first_tomb == (size_t)-1) {{
            first_tomb = idx;
        }}
        idx = (idx + 1) & mask;
    }}
}}

static inline HS_NODISCARD hs_status {name}_map_u64_resize_({name}_map_u64 *m, size_t new_cap) {{
    {name}_map_entry *old = m->entries;
    size_t old_cap = m->capacity;
    {name}_map_entry *fresh = ({name}_map_entry *)calloc(new_cap, sizeof(*fresh));
    if (fresh == NULL) {{ return HS_ERR_CAP; }}
    m->entries = fresh; m->capacity = new_cap; m->count = 0; m->tombstones = 0;
    for (size_t i = 0; i < old_cap; ++i) {{
        if (old[i].state == {NAME}_MAP_STATE_USED) {{
            size_t slot = {name}_map_find_slot_(m, old[i].key, 1);
            m->entries[slot].key = old[i].key;
            m->entries[slot].value = old[i].value;
            m->entries[slot].state = {NAME}_MAP_STATE_USED;
            m->count++;
        }}
    }}
    free(old);
    return HS_OK;
}}

static inline HS_NODISCARD hs_status {name}_map_u64_put({name}_map_u64 *m,
                                                        uint64_t key, {value} value) {{
    if ((m->count + m->tombstones + 1) * 8 > m->capacity * 7) {{
        hs_status s = {name}_map_u64_resize_(m, m->capacity * 2);
        if (s != HS_OK) {{ return s; }}
    }}
    size_t slot = {name}_map_find_slot_(m, key, 1);
    unsigned char st = m->entries[slot].state;
    if (st != {NAME}_MAP_STATE_USED) {{
        if (st == {NAME}_MAP_STATE_TOMBSTONE) {{ m->tombstones--; }}
        m->count++;
    }}
    m->entries[slot].key = key;
    m->entries[slot].value = value;
    m->entries[slot].state = {NAME}_MAP_STATE_USED;
    return HS_OK;
}}

static inline HS_NODISCARD hs_status {name}_map_u64_get(const {name}_map_u64 *m,
                                                        uint64_t key, {value} *out) {{
    if (m->capacity == 0) {{ return HS_ERR_BOUNDS; }}
    size_t slot = {name}_map_find_slot_(m, key, 0);
    if (m->entries[slot].state != {NAME}_MAP_STATE_USED) {{ return HS_ERR_BOUNDS; }}
    *out = m->entries[slot].value;
    return HS_OK;
}}

static inline unsigned char {name}_map_u64_remove({name}_map_u64 *m, uint64_t key) {{
    if (m->capacity == 0) {{ return 0; }}
    size_t slot = {name}_map_find_slot_(m, key, 0);
    if (m->entries[slot].state != {NAME}_MAP_STATE_USED) {{ return 0; }}
    m->entries[slot].state = {NAME}_MAP_STATE_TOMBSTONE;
    m->count--; m->tombstones++;
    return 1;
}}

#ifdef __cplusplus
}}
#endif
#endif
"""

