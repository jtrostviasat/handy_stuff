#!/usr/bin/env python3
"""
generate_container_header.py - emit a type-specific container header.

One invocation = one header = one container kind for one element type.

Examples:
    # Heap-backed dynamic array of int32_t
    python tools/generate_container_header.py \\
        --kind vec --type-name int32 --value-type int32_t \\
        --output include/handy_generated/int32_vec.h

    # Fixed-capacity ring buffer of struct event
    python tools/generate_container_header.py \\
        --kind ring --type-name event --value-type "struct event" \\
        --output include/handy_generated/event_ring.h

    # u64-keyed hash map of struct user *
    python tools/generate_container_header.py \\
        --kind map_u64 --type-name user --value-type "struct user *" \\
        --output include/handy_generated/user_map.h

    # hs_str-keyed hash map of int
    python tools/generate_container_header.py \\
        --kind map_str --type-name word_count --value-type int \\
        --output include/handy_generated/word_count_map.h

All emitted headers depend on `handy/handy_safety.h` (HS_OK / hs_status).
map_str additionally depends on `handy/handy_str.h`.
"""
from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path


def header_guard(path: Path) -> str:
    name = re.sub(r"[^A-Za-z0-9]", "_", path.name).upper()
    return f"HANDY_GEN_{name}"


VEC_TEMPLATE = r"""/* AUTO-GENERATED by tools/generate_container_header.py. Do not edit. */
#ifndef {guard}
#define {guard}

#include <stddef.h>
#include <stdlib.h>
#include <string.h>

#include "handy/handy_safety.h"

#ifdef __cplusplus
extern "C" {{
#endif

typedef struct {{
    {value} *data;
    size_t len;
    size_t capacity;
    unsigned char owns_heap;
}} {name}_vec;

static inline void {name}_vec_init({name}_vec *v) {{
    v->data = NULL; v->len = 0; v->capacity = 0; v->owns_heap = 1;
}}

static inline void {name}_vec_init_from_buffer({name}_vec *v,
                                                {value} *buffer, size_t capacity) {{
    v->data = buffer; v->len = 0; v->capacity = capacity; v->owns_heap = 0;
}}

static inline void {name}_vec_free({name}_vec *v) {{
    if (v->owns_heap && v->data != NULL) {{ free(v->data); }}
    v->data = NULL; v->len = 0; v->capacity = 0;
}}

static inline HS_NODISCARD hs_status {name}_vec_reserve({name}_vec *v, size_t needed) {{
    if (needed <= v->capacity) {{ return HS_OK; }}
    if (!v->owns_heap) {{ return HS_ERR_CAP; }}
    size_t new_cap = v->capacity == 0 ? 8 : v->capacity;
    while (new_cap < needed) {{ new_cap *= 2; }}
    {value} *p = ({value} *)realloc(v->data, new_cap * sizeof(*p));
    if (p == NULL) {{ return HS_ERR_CAP; }}
    v->data = p; v->capacity = new_cap;
    return HS_OK;
}}

static inline HS_NODISCARD hs_status {name}_vec_push({name}_vec *v, {value} value) {{
    hs_status s = {name}_vec_reserve(v, v->len + 1);
    if (s != HS_OK) {{ return s; }}
    v->data[v->len++] = value;
    return HS_OK;
}}

static inline unsigned char {name}_vec_pop({name}_vec *v, {value} *out) {{
    if (v->len == 0) {{ return 0; }}
    v->len--;
    if (out != NULL) {{ *out = v->data[v->len]; }}
    return 1;
}}

static inline HS_NODISCARD hs_status {name}_vec_get(const {name}_vec *v,
                                                    size_t index, {value} *out) {{
    if (index >= v->len) {{ return HS_ERR_BOUNDS; }}
    *out = v->data[index];
    return HS_OK;
}}

static inline HS_NODISCARD hs_status {name}_vec_set({name}_vec *v,
                                                    size_t index, {value} value) {{
    if (index >= v->len) {{ return HS_ERR_BOUNDS; }}
    v->data[index] = value;
    return HS_OK;
}}

static inline size_t {name}_vec_len(const {name}_vec *v) {{ return v->len; }}
static inline {value} *{name}_vec_data({name}_vec *v) {{ return v->data; }}
static inline void {name}_vec_clear({name}_vec *v) {{ v->len = 0; }}

#define {NAME}_VEC_FOR_EACH(it, v) \
    for (size_t _hs_i = 0; _hs_i < (v)->len && (((it) = (v)->data[_hs_i]), 1); ++_hs_i)

#ifdef __cplusplus
}}
#endif
#endif
"""

